/// Program which converts a number to byte format - Typecasting you know

class convertNumber
{
	public static void main(String args[])
	{
		/*
			int to float
			int to byte and viceversa
		*/
		int a = 128;
		int aByte = (byte)a;
		System.out.println("Integer " + a + "Byte Conversion " + aByte);
	}
}
